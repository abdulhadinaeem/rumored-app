                            ),
                          );
                        },
                      );
          },
          child: InkWell(
            child: Stack(
              children: [
                Container(
                  width: context.width * 0.36,
                  height: context.height * 0.17,
                  decoration: BoxDecoration(
                    color: index > 5
                        ? Colors.grey.shade400
                        : AppColors.textFiledColor,
                    borderRadius: const BorderRadius.all(
                      Radius.circular(22),
                    ),
                    image: DecorationImage(
