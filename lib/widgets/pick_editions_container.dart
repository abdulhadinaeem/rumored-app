                        title: "How do you want to create your quiz?",
                        buttonTitle1: 'By yourself',
                        buttonTitle2: 'Auto-Generated',
                        onPressed1: () {
                          Get.to(
                            const CreatQuizByYourselfScreen(),
                          );
