                        onPressed1: () {
                          Get.to(
                            PinJoinScreen(
                              isJoinScreen: false,
                              isAutoGeneratedScreen: false,
                              isMinor: false,
                              isSpinWheelScreen: true,
                              isWithoutMinorDrinking: false,
