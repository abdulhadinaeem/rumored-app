                        context,
                        isImage: false,
                        title: "How do you want to create your quiz?",
                        buttonTitle1: 'By yourself',
                        buttonTitle2: 'Auto-Generated',
                        onPressed1: () {
                          Get.to(
                            PinJoinScreen(
                              isByYourself: true,
                              isJoinScreen: false,
                              isAutoParticipantsDrinking: false,
                              isAutoGeneratedScreen: false,
                              isWithoutMinorDrinking: false,
                              isMinor: false,
                              isSpinWheelScreen: false,
                              isDrinkingGame: true,
                            ),
                          );
                        },
                        onPressed2: () {
                          Get.to(
                            () => PinJoinScreen(
                              isWithoutMinorDrinking: false,
                              isJoinScreen: false,
                              isAutoParticipantsDrinking: true,
                              isAutoGeneratedScreen: true,
                              isByYourself: false,
                              isDrinkingGame: false,
                              isMinor: false,
                              isSpinWheelScreen: false,
                            ),
                          );
                        },
                      );
